    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String turno = lstTurnos.getSelectedItem().toString();
        String curso = lstCursos.getSelectedItem().toString();
        String registro = lstAsistencia.getSelectedItem().toString();

        Asistencia asistencia
                = new Asistencia
        (nombre, apellido, turno, curso,registro);
        this.padre.agregarEstudiantes(asistencia);
        cerrar();
    }//GEN-LAST:event_btnGuardarActionPerformed

